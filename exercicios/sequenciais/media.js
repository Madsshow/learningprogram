//Faça um Programa que peça as 4 notas bimestrais e mostre a média.
var media1 = parseInt(prompt("Digite a primeira média"));
var media2 = parseInt(prompt("Digite a segunda média"));
var media3 = parseInt(prompt("Digite a terceira média"));
var media4 = parseInt(prompt("Digite a quarta média"));
var media = (media1 + media2 + media3 + media4) / 4

console.log("A nota 1 é:", media1)
console.log("A nota 2 é:", media2)
console.log("A nota 3 é:", media3)
console.log("A nota 4 é:", media4)
console.log("A média total é:", media)